public class Exercise08_WhileLoop_Liftoff {

	/**
	 * @param args
	 */
	public static void main(String[] args) {

		//Declare and Initialise Variables
		
		int i = 10;
		
		while (i > 0) {
			System.out.print(i + ", ");
			i--;
		}
		System.out.print("Liftoff!");
	}

}
